package spring.data;

public class HotelDto {
	
	private int hotel_num;
	private String hotel_local;
	private String hotel_tel;
	private String hotel_img;
	private String hotel_desc;
	
	
	public int getHotel_num() {
		return hotel_num;
	}
	public void setHotel_num(int hotel_num) {
		this.hotel_num = hotel_num;
	}
	public String getHotel_local() {
		return hotel_local;
	}
	public void setHotel_local(String hotel_local) {
		this.hotel_local = hotel_local;
	}
	public String getHotel_tel() {
		return hotel_tel;
	}
	public void setHotel_tel(String hotel_tel) {
		this.hotel_tel = hotel_tel;
	}
	public String getHotel_img() {
		return hotel_img;
	}
	public void setHotel_img(String hotel_img) {
		this.hotel_img = hotel_img;
	}
	public String getHotel_desc() {
		return hotel_desc;
	}
	public void setHotel_desc(String hotel_desc) {
		this.hotel_desc = hotel_desc;
	}
	
	
	
	
}
